import type { Metadata } from "next";
import { Inter, Noto_Sans_KR } from "next/font/google";
import "@/styles/globals.css";
import { cookies } from 'next/headers';
import { RequestCookie } from "next/dist/compiled/@edge-runtime/cookies";
import LayoutWrapper from "./LayoutWrapper";

const inter = Inter({ subsets: ["latin"] });
const notoSans = Noto_Sans_KR({
  preload: false,
  style: 'normal',
});

export const metadata: Metadata = {
  title: 'Toeicdoit',
  description: 'Generated by create next app',
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {

  const cookieStore = cookies();
  const token: RequestCookie | undefined = cookieStore.get('token');
  
  return (
    <html lang="en">

      <body className={`${inter.className} ${notoSans.className}`}>

        <div className="bg-white overflow-hidden sm:px-[5%] md:px-[5%] lg:px-[8%]">
        <LayoutWrapper token={token}>{children}</LayoutWrapper>

        </div>


      </body>
    </html>
  );
}
